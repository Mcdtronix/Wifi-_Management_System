"""
apps/reporting/admin.py
------------------------
Django admin for analytics dashboards, revenue summaries, and subscriber reports.
All reporting data is read-only — generated by Celery Beat tasks.
"""

from django.contrib import admin
from django.utils.html import format_html
from django.utils.translation import gettext_lazy as _

from .models import DashboardMetrics, RevenueSummary, SubscriberReport


@admin.register(DashboardMetrics)
class DashboardMetricsAdmin(admin.ModelAdmin):
    list_display = (
        "metrics_date", "active_subscribers", "online_now",
        "sessions_today", "revenue_usd",
        "total_traffic_gb", "fraud_alerts", "created_at",
    )
    date_hierarchy = "metrics_date"
    ordering = ("-metrics_date",)
    readonly_fields = tuple(
        f.name for f in DashboardMetrics._meta.get_fields()
        if hasattr(f, "name") and not f.many_to_many
    )

    fieldsets = (
        (_("Date"), {"fields": ("metrics_date",)}),
        (_("Subscribers"), {
            "fields": (
                "total_subscribers", "active_subscribers", "expired_subscribers",
                "suspended_subscribers", "new_subscribers_today", "churned_subscribers",
            ),
        }),
        (_("Sessions"), {
            "fields": ("online_now", "sessions_today", "avg_session_minutes"),
        }),
        (_("Traffic"), {
            "fields": ("total_upload_gb", "total_download_gb", "total_traffic_gb", "peak_hour"),
        }),
        (_("Quota"), {
            "fields": ("quota_exceeded_count", "quota_warned_count"),
        }),
        (_("Revenue"), {
            "fields": ("revenue_usd", "new_plans_sold", "renewals_today", "vouchers_redeemed"),
        }),
        (_("Errors & Fraud"), {
            "fields": ("failed_logins", "fraud_alerts", "system_errors"),
        }),
        (_("Devices & Admin"), {
            "fields": ("unique_devices", "device_changes_requested", "admin_actions_today"),
        }),
    )

    def has_add_permission(self, request):
        return False  # Generated by Celery Beat — do not create manually

    def has_change_permission(self, request, obj=None):
        return False


