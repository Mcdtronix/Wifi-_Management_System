@admin.register(SubscriberReport)
class SubscriberReportAdmin(admin.ModelAdmin):
    list_display = (
        "subscriber", "report_period_start", "report_period_end",
        "total_gb_display", "total_spent_usd",
        "sessions_count", "is_sent", "generated_at",
    )
    list_filter = ("is_sent",)
    search_fields = ("subscriber__username", "subscriber__full_name")
    date_hierarchy = "generated_at"
    ordering = ("-generated_at",)
    readonly_fields = tuple(
        f.name for f in SubscriberReport._meta.get_fields()
        if hasattr(f, "name") and not f.many_to_many
    )
    raw_id_fields = ("subscriber",)

    fieldsets = (
        (_("Report"), {
            "fields": ("subscriber", "report_period_start", "report_period_end", "generated_at"),
        }),
        (_("Activity"), {
            "fields": ("sessions_count", "total_session_hours", "last_active_at"),
        }),
        (_("Usage"), {
            "fields": ("upload_gb", "download_gb", "total_gb", "quota_limit_gb", "quota_usage_percent"),
        }),
        (_("Spending"), {
            "fields": ("plans_active", "renewals_count", "total_spent_usd"),
        }),
        (_("Alerts"), {
            "fields": ("quota_warnings", "failed_logins", "fraud_alerts"),
        }),
        (_("Delivery"), {
            "fields": ("is_sent", "sent_at"),
        }),
    )

    def has_add_permission(self, request):
        return False  # Reports are generated via API action or Celery task

    def has_change_permission(self, request, obj=None):
        return False

    @admin.display(description=_("Total Used"))
    def total_gb_display(self, obj):
        return format_html("{:.2f} <small>GB</small>", obj.total_gb)
